import { readFile, writeFile } from "node:fs/promises";

const source = new URL("../wrangler.local.toml", import.meta.url);
const target = new URL("../wrangler.toml", import.meta.url);
const marker = "credit-bond-process-local";

async function main() {
  const localConfig = await readFile(source, "utf8");
  let existing = "";
  try {
    existing = await readFile(target, "utf8");
  } catch {
    // Missing is fine.
  }

  if (existing && !existing.includes(marker)) {
    throw new Error("wrangler.toml already exists and does not look like the local dev config. Refusing to overwrite it.");
  }

  await writeFile(target, `# Generated by tools/prepare-local-dev.mjs for local branch previews.\n# This file is ignored by git. Production Pages settings still come from Cloudflare.\n\n${localConfig}`);
  console.log("Prepared local wrangler.toml for D1 binding DB.");
}

main().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});
